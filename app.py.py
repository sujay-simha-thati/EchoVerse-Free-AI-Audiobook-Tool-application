import streamlit as st
from gtts import gTTS
import os

st.set_page_config(page_title="EchoVerse â€“ AI Audiobook Tool", page_icon="ğŸ§")

st.title("ğŸ§ EchoVerse â€“ AI Audiobook Creator")
st.write("Convert your text or `.txt` files into audiobooks using AI Text-to-Speech.")

# --- User input ---
text_input = st.text_area("âœï¸ Enter text below:")

uploaded_file = st.file_uploader("ğŸ“‚ Or upload a .txt file", type=["txt"])

# Extract text from uploaded file if present
file_text = ""
if uploaded_file is not None:
    file_text = uploaded_file.read().decode("utf-8")

# Combine inputs
final_text = text_input if text_input.strip() else file_text

# --- Generate audio ---
if st.button("ğŸ™ï¸ Generate Audio"):
    if final_text.strip():
        tts = gTTS(final_text)
        output_path = "output.mp3"
        tts.save(output_path)

        st.success("âœ… Audiobook generated successfully!")
        audio_file = open(output_path, "rb")
        st.audio(audio_file.read(), format="audio/mp3")

        # Download button
        st.download_button(
            label="â¬‡ï¸ Download MP3",
            data=open(output_path, "rb"),
            file_name="audiobook.mp3",
            mime="audio/mp3"
        )
    else:
        st.warning("Please enter text or upload a `.txt` file before generating.")
